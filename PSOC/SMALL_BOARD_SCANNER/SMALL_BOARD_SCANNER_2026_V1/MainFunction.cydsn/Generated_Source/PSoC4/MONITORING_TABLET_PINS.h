/* Set bits-mask in register */
#define MONITORING_TABLET_SET_REGISTER_BITS(reg, mask, pos, mode) \
                    do                                           \
                    {                                            \
                        (reg) = (((reg) & ((uint32) ~(uint32) (mask))) | ((uint32) ((uint32) (mode) << (pos)))); \
                    }while(0)

/* Set bit in the register */
#define MONITORING_TABLET_SET_REGISTER_BIT(reg, mask, val) \
                    ((val) ? ((reg) |= (mask)) : ((reg) &= ((uint32) ~((uint32) (mask)))))

#define MONITORING_TABLET_SET_HSIOM_SEL(reg, mask, pos, sel) MONITORING_TABLET_SET_REGISTER_BITS(reg, mask, pos, sel)
#define MONITORING_TABLET_SET_INCFG_TYPE(reg, mask, pos, intType) \
                                                        MONITORING_TABLET_SET_REGISTER_BITS(reg, mask, pos, intType)
#define MONITORING_TABLET_SET_INP_DIS(reg, mask, val) MONITORING_TABLET_SET_REGISTER_BIT(reg, mask, val)

